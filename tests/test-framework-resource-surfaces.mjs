import assert from 'node:assert/strict';

import {
  FRAMEWORK_RESOURCE_SURFACE_POLICY_VERSION,
  classifyFrameworkResourceSurfaces,
} from '../_lib/framework-resource-surfaces.mjs';

let passed = 0;
let failed = 0;

function check(label, fn) {
  try {
    fn();
    passed++;
    console.log(`  PASS  ${label}`);
  } catch (error) {
    failed++;
    console.log(`  FAIL  ${label}\n        ${error?.message ?? error}`);
  }
}

function lanes(artifact, file) {
  return artifact.files.find((entry) => entry.file === file)?.surfaceLanes ?? [];
}

function firstLane(artifact, file) {
  const lane = lanes(artifact, file)[0];
  assert.ok(lane, `expected surface lane for ${file}`);
  return lane;
}

check('FRS1. classifies grounded Storybook and Strapi dispatch surfaces', () => {
  const artifact = classifyFrameworkResourceSurfaces({
    root: '/repo',
    files: [
      'src/Button.stories.tsx',
      'src/api/article/controllers/article.ts',
      'src/api/article/routes/article.ts',
      'src/plain.ts',
    ],
    packageRecords: [
      {
        relRoot: '.',
        packageJson: {
          devDependencies: { '@storybook/react': '^8.0.0' },
          dependencies: { '@strapi/strapi': '^5.0.0' },
        },
      },
    ],
  });

  assert.equal(artifact.schemaVersion, 'framework-resource-surfaces.v1');
  assert.equal(artifact.policyVersion, FRAMEWORK_RESOURCE_SURFACE_POLICY_VERSION);

  assert.deepEqual(firstLane(artifact, 'src/Button.stories.tsx'), {
    lane: 'framework-dispatch-entry',
    capabilityPack: 'framework.storybook',
    confidence: 'grounded',
    framework: 'storybook',
    reason: 'storybook-story-file',
    defaultAction: 'review-hint',
    affectsAbsenceClaims: true,
    evidence: [
      { kind: 'dependency', field: 'devDependencies.@storybook/react' },
      { kind: 'path-convention', matched: '*.stories.*' },
    ],
  });

  const controller = firstLane(artifact, 'src/api/article/controllers/article.ts');
  assert.equal(controller.lane, 'framework-dispatch-entry');
  assert.equal(controller.capabilityPack, 'framework.strapi');
  assert.equal(controller.confidence, 'grounded');
  assert.equal(controller.framework, 'strapi');
  assert.equal(controller.reason, 'strapi-filesystem-api');

  const route = firstLane(artifact, 'src/api/article/routes/article.ts');
  assert.equal(route.capabilityPack, 'framework.strapi');
  assert.equal(route.framework, 'strapi');
  assert.equal(route.confidence, 'grounded');

  assert.equal(lanes(artifact, 'src/plain.ts').length, 0);
  assert.equal(artifact.summary.totalFilesWithSurfaces, 3);
  assert.equal(artifact.summary.byLane['framework-dispatch-entry'], 3);
  assert.equal(artifact.summary.byCapabilityPack['framework.storybook'], 1);
  assert.equal(artifact.summary.byCapabilityPack['framework.strapi'], 2);
  assert.equal(artifact.summary.byConfidence.grounded, 3);
});

check('FRS2. keeps path-only framework shapes as review-visible, not grounded', () => {
  const artifact = classifyFrameworkResourceSurfaces({
    root: '/repo',
    files: [
      'src/Button.stories.tsx',
      'src/api/article/controllers/article.ts',
    ],
    packageRecords: [{ relRoot: '.', packageJson: { dependencies: {} } }],
  });

  for (const file of ['src/Button.stories.tsx', 'src/api/article/controllers/article.ts']) {
    const lane = firstLane(artifact, file);
    assert.equal(lane.lane, 'framework-dispatch-entry');
    assert.equal(lane.confidence, 'path-shaped-review');
    assert.equal(lane.defaultAction, 'review-hint');
  }
});

check('FRS3. classifies generated declarations, bundles, templates, and codemod resources', () => {
  const artifact = classifyFrameworkResourceSurfaces({
    root: '/repo',
    files: [
      'types/generated/contentTypes.d.ts',
      'public/vendor.js',
      'dist/app.bundle.js',
      'dist/app.min.js',
      'src/emscripten-bindings.js',
      'templates/component.tsx.hbs',
      'resources/codemods/rename/input.ts',
      '__testfixtures__/fixture.ts',
    ],
    packageRecords: [{ relRoot: '.', packageJson: {} }],
    contentsByFile: {
      'src/emscripten-bindings.js':
        '// @ts-nocheck\n// Generated by Emscripten\nexport const Module = {};\n',
    },
  });

  assert.equal(firstLane(artifact, 'types/generated/contentTypes.d.ts').lane, 'generated-declaration-surface');
  assert.equal(firstLane(artifact, 'types/generated/contentTypes.d.ts').capabilityPack, 'surface.generated-declaration');
  assert.equal(firstLane(artifact, 'types/generated/contentTypes.d.ts').confidence, 'generated-output-review');

  for (const file of ['public/vendor.js', 'dist/app.bundle.js', 'dist/app.min.js']) {
    const lane = firstLane(artifact, file);
    assert.equal(lane.lane, 'bundled-build-artifact');
    assert.equal(lane.capabilityPack, 'surface.bundled-build-artifact');
    assert.equal(lane.confidence, 'generated-output-review');
  }

  const emscripten = firstLane(artifact, 'src/emscripten-bindings.js');
  assert.equal(emscripten.lane, 'bundled-build-artifact');
  assert.equal(emscripten.capabilityPack, 'surface.bundled-build-artifact');
  assert.equal(emscripten.reason, 'emscripten-generated-header');

  assert.equal(firstLane(artifact, 'templates/component.tsx.hbs').lane, 'scaffold-template-resource');
  assert.equal(firstLane(artifact, 'templates/component.tsx.hbs').capabilityPack, 'surface.scaffold-template');
  assert.equal(firstLane(artifact, 'resources/codemods/rename/input.ts').lane, 'codemod-resource');
  assert.equal(firstLane(artifact, 'resources/codemods/rename/input.ts').capabilityPack, 'surface.codemod-resource');
  assert.equal(firstLane(artifact, '__testfixtures__/fixture.ts').lane, 'codemod-resource');
  assert.equal(firstLane(artifact, '__testfixtures__/fixture.ts').capabilityPack, 'surface.codemod-resource');

  assert.equal(artifact.summary.totalFilesWithSurfaces, 8);
  assert.equal(artifact.summary.byLane['bundled-build-artifact'], 4);
  assert.equal(artifact.summary.byLane['codemod-resource'], 2);
  assert.equal(artifact.summary.byCapabilityPack['surface.bundled-build-artifact'], 4);
  assert.equal(artifact.summary.byCapabilityPack['surface.codemod-resource'], 2);
  assert.equal(artifact.summary.byCapabilityPack['surface.generated-declaration'], 1);
  assert.equal(artifact.summary.byCapabilityPack['surface.scaffold-template'], 1);
});

check('FRS4. output is deterministic and sorted by file path', () => {
  const artifact = classifyFrameworkResourceSurfaces({
    root: '/repo',
    files: [
      'templates/z.hbs',
      'src/B.stories.tsx',
      'resources/codemods/a.ts',
    ],
    packageRecords: [
      {
        relRoot: '.',
        packageJson: { devDependencies: { storybook: '^8.0.0' } },
      },
    ],
  });

  assert.deepEqual(artifact.files.map((entry) => entry.file), [
    'resources/codemods/a.ts',
    'src/B.stories.tsx',
    'templates/z.hbs',
  ]);
});

if (failed > 0) {
  console.error(`\n${passed} passed, ${failed} failed`);
  process.exit(1);
}

console.log(`\n${passed} passed, 0 failed`);
