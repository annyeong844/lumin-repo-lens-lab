import assert from 'node:assert/strict';
import { execFileSync } from 'node:child_process';
import { mkdirSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

let passed = 0;
let failed = 0;

function check(label, fn) {
  try {
    fn();
    passed++;
    console.log(`  PASS  ${label}`);
  } catch (error) {
    failed++;
    console.log(`  FAIL  ${label}\n        ${error?.message ?? error}`);
  }
}

function write(root, rel, content) {
  const full = path.join(root, rel);
  mkdirSync(path.dirname(full), { recursive: true });
  writeFileSync(full, content);
}

function makeFixture() {
  const dir = mkdtempSync(path.join(os.tmpdir(), 'lrl-framework-surfaces-'));
  const out = path.join(dir, '.audit');
  mkdirSync(out, { recursive: true });
  write(dir, 'package.json', JSON.stringify({
    name: 'framework-resource-fixture',
    private: true,
    type: 'module',
    devDependencies: { '@storybook/react': '^8.0.0' },
    dependencies: { '@strapi/strapi': '^5.0.0' },
  }, null, 2));
  write(dir, 'src/Button.stories.tsx', 'export default { title: "Button" };\n');
  write(dir, 'src/api/article/controllers/article.ts', 'export default { find() {} };\n');
  write(dir, 'types/generated/contentTypes.d.ts', 'export interface Article {}\n');
  write(dir, 'public/vendor.js', 'window.vendor = true;\n');
  write(dir, 'src/app.bundle.js', 'export const bundled = true;\n');
  write(dir, 'src/emscripten-bindings.js', '// @ts-nocheck\n// Generated by Emscripten\n');
  write(dir, 'templates/controller.ts.hbs', 'export default {};\n');
  write(dir, 'resources/codemods/rename/input.ts', 'export const input = true;\n');
  write(dir, 'src/plain.ts', 'export const plain = true;\n');
  return { dir, out };
}

function runProducer() {
  const { dir, out } = makeFixture();
  try {
    execFileSync(process.execPath, [
      path.join(ROOT, 'build-framework-resource-surfaces.mjs'),
      '--root', dir,
      '--output', out,
    ], { encoding: 'utf8' });
    return JSON.parse(readFileSync(path.join(out, 'framework-resource-surfaces.json'), 'utf8'));
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
}

function runAudit() {
  const { dir, out } = makeFixture();
  try {
    execFileSync(process.execPath, [
      path.join(ROOT, 'audit-repo.mjs'),
      '--root', dir,
      '--output', out,
      '--profile', 'quick',
    ], { encoding: 'utf8' });
    return {
      manifest: JSON.parse(readFileSync(path.join(out, 'manifest.json'), 'utf8')),
      surfaces: JSON.parse(readFileSync(path.join(out, 'framework-resource-surfaces.json'), 'utf8')),
      summary: readFileSync(path.join(out, 'audit-summary.latest.md'), 'utf8'),
    };
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
}

check('BFRS1. producer writes framework-resource-surfaces artifact', () => {
  const artifact = runProducer();
  assert.equal(artifact.schemaVersion, 'framework-resource-surfaces.v1');
  assert.equal(artifact.policyVersion, 'framework-resource-surface-policy-v1');
  assert.equal(artifact.summary.totalFilesWithSurfaces, 8);
  assert.equal(artifact.summary.byLane['framework-dispatch-entry'], 2);
  assert.equal(artifact.summary.byLane['bundled-build-artifact'], 3);
  assert.equal(artifact.summary.byLane['generated-declaration-surface'], 1);
  assert.equal(artifact.summary.byLane['scaffold-template-resource'], 1);
  assert.equal(artifact.summary.byLane['codemod-resource'], 1);
  assert.equal(artifact.summary.byCapabilityPack['framework.storybook'], 1);
  assert.equal(artifact.summary.byCapabilityPack['framework.strapi'], 1);
  assert.equal(artifact.summary.byCapabilityPack['surface.bundled-build-artifact'], 3);
  assert.equal(artifact.summary.byCapabilityPack['surface.generated-declaration'], 1);
  assert.equal(artifact.summary.byCapabilityPack['surface.scaffold-template'], 1);
  assert.equal(artifact.summary.byCapabilityPack['surface.codemod-resource'], 1);
  assert.ok(artifact.files.some((entry) =>
    entry.file === 'src/Button.stories.tsx' &&
    entry.surfaceLanes.some((lane) => lane.capabilityPack === 'framework.storybook')));
  assert.ok(artifact.files.some((entry) =>
    entry.file === 'templates/controller.ts.hbs' &&
    entry.surfaceLanes.some((lane) => lane.capabilityPack === 'surface.scaffold-template')));
});

check('BFRS2. audit-repo runs producer and surfaces manifest/summary metadata', () => {
  const { manifest, surfaces, summary } = runAudit();
  assert.ok(manifest.commandsRun.some((entry) =>
    entry.step === 'build-framework-resource-surfaces.mjs' &&
    entry.status === 'ok'));
  assert.ok(manifest.artifactsProduced.includes('framework-resource-surfaces.json'));
  assert.equal(manifest.frameworkResourceSurfaces?.totalFilesWithSurfaces, 8);
  assert.equal(manifest.frameworkResourceSurfaces?.byLane?.['framework-dispatch-entry'], 2);
  assert.equal(surfaces.summary.totalFilesWithSurfaces, 8);
  assert.match(summary, /Framework\/resource surfaces: 8 files/);
  assert.match(summary, /framework-resource-surfaces\.json/);
});

if (failed > 0) {
  console.error(`\n${passed} passed, ${failed} failed`);
  process.exit(1);
}

console.log(`\n${passed} passed, 0 failed`);
