use lumin_rust_common::posix_path_has_segment;

use crate::protocol::PathClassification;

pub(super) fn classify_path(path: &str, source: &str) -> Vec<PathClassification> {
    if is_generated_path(path) || has_generated_header(source) {
        vec![PathClassification::Generated]
    } else if is_test_like_path(path) {
        vec![PathClassification::Test]
    } else {
        vec![PathClassification::Source]
    }
}

fn is_generated_path(path: &str) -> bool {
    let base = file_name(path);
    posix_path_has_segment(path, "generated")
        || posix_path_has_segment(path, "__generated__")
        || base == "generated.rs"
        || base.ends_with(".gen.rs")
        || base.ends_with(".generated.rs")
}

fn has_generated_header(source: &str) -> bool {
    let header = source_header(source, 2048).to_lowercase();
    header.contains("@generated")
        || header.contains("<auto-generated")
        || header.contains("auto-generated")
        || header.contains("generated by")
        || header.contains("this file is generated")
}

fn source_header(source: &str, max_bytes: usize) -> &str {
    if source.len() <= max_bytes {
        return source;
    }
    let mut end = 0;
    for (index, _) in source.char_indices() {
        if index > max_bytes {
            break;
        }
        end = index;
    }
    &source[..end]
}

fn is_test_like_path(path: &str) -> bool {
    let base = file_name(path);
    if base == "tests.rs"
        || base == "test.rs"
        || base.ends_with("_test.rs")
        || base.ends_with("_tests.rs")
        || base.ends_with(".test.rs")
        || base.ends_with(".spec.rs")
    {
        return true;
    }

    path.split('/').any(|segment| {
        matches!(
            segment,
            "test"
                | "tests"
                | "e2e"
                | "integration"
                | "fixtures"
                | "fixture"
                | "mocks"
                | "mock"
                | "test-support"
                | "test-utils"
                | "runtime-tests"
                | "playground"
                | "playgrounds"
                | "examples"
                | "example"
                | "benches"
                | "bench"
        ) || (segment.len() >= 4 && segment.starts_with("__") && segment.ends_with("__"))
            || segment.ends_with("-fixture")
            || segment.ends_with("-fixtures")
    })
}

fn file_name(path: &str) -> &str {
    path.rsplit_once('/').map_or(path, |(_, name)| name)
}
